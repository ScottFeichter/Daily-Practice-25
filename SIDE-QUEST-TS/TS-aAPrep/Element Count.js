// Element Count

//--------------------------------------------------------------------------PROBLEM---------------------------------------------------------------//

// Write a function elementCount that takes in an array and returns an object representing the count of each element in the array.

//-------------------------------------------------------------------------SOLUTION---------------------------------------------------------------//

function elementCount(arr) {
  let outObj = {};
  for (let v of arr) {
    if (!outObj[v]) {
      outObj[v] = 1;
    } else if (outObj[v]) {
      outObj[v]++;
    }
  }
  return outObj;
}

//-------------------------------------------------------------------------TEST CASES---------------------------------------------------------------//

console.log(elementCount(["a", "b", "a", "a", "b"])); // {"a" : 3, "b" : 2}
console.log(elementCount(["red", "red", "blue", "green"])); // {"red" : 2, "blue" : 1, "green" : 1}

