// Unique Elements

//--------------------------------------------------------------------------PROBLEM---------------------------------------------------------------//

// Write a function uniqueElements that takes in an array and returns a new array where all duplicate elements are removed.
// Solve this using an object.

//-------------------------------------------------------------------------SOLUTION---------------------------------------------------------------//

function uniqueElements(array) {
  let outArr = [];
  let obj = {};
  for (let v of array) {
    if (obj[v]) {
      obj[v]++;
    } else if (!obj[v]) {
      obj[v] = 1;
    }
  }

  for (let k in obj) {
    outArr.push(k);
  }

  return outArr;
}

//-------------------------------------------------------------------------TEST CASES---------------------------------------------------------------//

console.log(uniqueElements (['a', 'b', 'a', 'a', 'b', 'c'])) // ["a", "b", "c"]

