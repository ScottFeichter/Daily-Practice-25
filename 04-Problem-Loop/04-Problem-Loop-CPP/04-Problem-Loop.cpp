// create a mutable variable sum = 1 and print it then create a basic for loop and increment sum 9 times to total 10 then print sum

#include <iostream>
using namespace std;

int main() {

  int sum = 1;
  cout << sum << endl;

  while (sum < 10) {
    sum++;
  }

  cout << sum << endl;


 return 0;
}
