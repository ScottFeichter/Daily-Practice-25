// Whisper Words

//--------------------------------------------------------------------------PROBLEM---------------------------------------------------------------//

// Write a function whisperWords that takes in an array of words and returns a new array containing a whispered version of each word.
// See the examples. Solve this using map :).

//-------------------------------------------------------------------------SOLUTION---------------------------------------------------------------//

function whisperWords(arr) {
  let outArr = [];
  for (let v of arr) {
    outArr.push(v.toLowerCase() + "...");
  }
  return outArr;
}

//-------------------------------------------------------------------------TEST CASES---------------------------------------------------------------//

console.log(whisperWords(["KEEP", "The", "NOISE", "down"])); // ["keep...", "the...", "noise...", "down..."]

