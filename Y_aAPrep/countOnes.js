//--------------------------------------------------------------------------TITLE---------------------------------------------------------------//

// Count Ones

//--------------------------------------------------------------------------PROBLEM---------------------------------------------------------------//

// input arr of numbers only zeros and ones
// return longest number of consecutive ones

//-------------------------------------------------------------------------SOLUTION---------------------------------------------------------------//

function countOnes(arr) {
  let count = 0;
  let highest = 0;
  for (let v of arr) {
    if (v === 1) {
      count++
    } else {
      if (count > highest) {
        highest = count;
        count = 0;
      } else {
        count = 0;
      }
    }
  }
  if (count > highest) {
    highest = count;
  }
  return highest;
}

//-------------------------------------------------------------------------TEST CASES---------------------------------------------------------------//

countOnes([1,1,0,0,1,1,1,1,0,0,1,1,1,1,1]) // 5
countOnes([1,1,0,0,0,1,1]) // 2
countOnes([1,0,0,0,0,0,0]) // 1

