// deDupe

//--------------------------------------------------------------------------PROBLEM---------------------------------------------------------------//

// assume arr only num as parameter remove duplicates
// return new arr with same numbers in same order without duplicates

//-------------------------------------------------------------------------SOLUTION---------------------------------------------------------------//

function deDupe(arr) {
  let nuArr = [];
  for (let i = 0; i < arr.length; i++) {
    if (!nuArr.includes(arr[i])) {
      nuArr.push(arr[i]);
    }
  }
  return nuArr;
}

//-------------------------------------------------------------------------TEST CASES---------------------------------------------------------------//

deDupe([1,2,1,3,2]) // [1,2,3]

