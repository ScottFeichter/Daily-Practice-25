//--------------------------------------------------------------------------PROBLEM---------------------------------------------------------------//

// Write a function that takes in a string and returns an object counting number of times each letter occurs.

//-------------------------------------------------------------------------SOLUTION---------------------------------------------------------------//

function letterCounter(string) {
  let count = {};
  for (let i = 0; i < string.length; i++) {
    if (count[string[i]]) {
      count[string[i]] += 1;
    } else {
      count[string[i]] = 1;
    }
  }
  return count;
}

